 //expressão de adição, e o valor fica atribuido na variável: s
 var s = 1 + 1;
//incrementar 1
s = s + 1;
//incremento em 1
s++;    //pós incremento

++s;    //pre incremento

s--;    //pós decremento

--s;    //pré decremento

//opradores abrevidados
s += 1;     //equivalente a: s = s + 1;

s -= 1;     //equivalente a: s = s - 1;

s *= 1;     //s = s * 1;

s /= 1;     //s = s / 1;
