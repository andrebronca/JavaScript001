/* operador unário */
+1;     //não faz a conversão

+'1';   //converte a string em número

+'um';  //NaN

'java' + 'script';  //realiza a concatenação

'1' + 1;    // retorna: 11

1 + '1';    // retorna: 11

-1;     //transforma em nr negativo

var x = 1;
-x;         //exibe: -1. Não altera o valor

-'1';       //retorna: -1

x++;    //exibe o valor de x e incrementa
++x;    //incrementa e depois exibe o valor. Exibe = valor pronto para uso.
